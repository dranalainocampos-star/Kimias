import Database from "better-sqlite3";
import postgres from "postgres";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, "..");
const sqlitePath = process.env.SQLITE_SOURCE_PATH || path.join(rootDir, "data.sqlite");
const databaseUrl = process.env.DATABASE_URL || process.env.POSTGRES_URL || "";

if (!databaseUrl) {
  console.error("Missing DATABASE_URL. Add your Supabase Postgres connection string before running this migration.");
  process.exit(1);
}

function postgresSslOption() {
  const override = String(process.env.POSTGRES_SSL || "").trim().toLowerCase();
  if (["0", "false", "off", "disable", "disabled"].includes(override)) return false;
  if (override) return "require";
  return /(?:localhost|127\.0\.0\.1)/i.test(databaseUrl) ? false : "require";
}

function normalizeTimestamp(value) {
  if (!value) return new Date().toISOString();
  return value;
}

const source = new Database(sqlitePath, { readonly: true, fileMustExist: true });
const sql = postgres(databaseUrl, {
  connect_timeout: 10,
  idle_timeout: 20,
  max: Number(process.env.POSTGRES_MAX_CONNECTIONS || 5),
  prepare: false,
  ssl: postgresSslOption(),
});

async function createSchema() {
  await sql`
    CREATE TABLE IF NOT EXISTS posts (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      title TEXT NOT NULL,
      category TEXT NOT NULL,
      location TEXT NOT NULL DEFAULT '',
      category_color TEXT NOT NULL DEFAULT 'sand',
      image TEXT NOT NULL DEFAULT '',
      supporting_images TEXT NOT NULL DEFAULT '[]',
      slug TEXT NOT NULL UNIQUE,
      content TEXT NOT NULL DEFAULT '',
      excerpt TEXT NOT NULL DEFAULT '',
      published_at TEXT NOT NULL DEFAULT '',
      status TEXT NOT NULL DEFAULT 'Draft',
      created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
      updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
    )
  `;

  await sql`
    CREATE TABLE IF NOT EXISTS comments (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      post_id INTEGER NOT NULL REFERENCES posts(id) ON DELETE CASCADE,
      author TEXT NOT NULL,
      email TEXT NOT NULL,
      text TEXT NOT NULL,
      status TEXT NOT NULL DEFAULT 'Pending',
      reply TEXT NOT NULL DEFAULT '',
      created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
      updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
    )
  `;

  await sql`CREATE INDEX IF NOT EXISTS idx_comments_post_id ON comments(post_id)`;
  await sql`CREATE INDEX IF NOT EXISTS idx_comments_status ON comments(status)`;

  await sql`
    CREATE TABLE IF NOT EXISTS contacts (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      name TEXT NOT NULL,
      email TEXT NOT NULL,
      message TEXT NOT NULL,
      status TEXT NOT NULL DEFAULT 'Unread',
      created_at TIMESTAMPTZ NOT NULL DEFAULT now()
    )
  `;

  await sql`
    CREATE TABLE IF NOT EXISTS newsletter_subscribers (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      email TEXT NOT NULL UNIQUE,
      source TEXT NOT NULL DEFAULT 'blog',
      status TEXT NOT NULL DEFAULT 'Subscribed',
      created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
      updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
    )
  `;
}

async function resetIdentity(table) {
  await sql.unsafe(
    `SELECT setval(pg_get_serial_sequence('${table}', 'id'), COALESCE((SELECT MAX(id) FROM ${table}), 1), true)`,
  );
}

async function migrate() {
  const posts = source.prepare("SELECT * FROM posts ORDER BY id ASC").all();
  const comments = source.prepare("SELECT * FROM comments ORDER BY id ASC").all();
  const contacts = source.prepare("SELECT * FROM contacts ORDER BY id ASC").all();
  const subscribers = source.prepare("SELECT * FROM newsletter_subscribers ORDER BY id ASC").all();

  await createSchema();

  await sql.begin(async (tx) => {
    for (const post of posts) {
      await tx`
        INSERT INTO posts (
          id, title, category, location, category_color, image, supporting_images, slug, content, excerpt,
          published_at, status, created_at, updated_at
        ) VALUES (
          ${post.id}, ${post.title}, ${post.category}, ${post.location}, ${post.category_color}, ${post.image},
          ${post.supporting_images || "[]"}, ${post.slug}, ${post.content || post.excerpt || ""},
          ${post.excerpt || ""}, ${post.published_at || ""}, ${post.status || "Draft"},
          ${normalizeTimestamp(post.created_at)}, ${normalizeTimestamp(post.updated_at)}
        )
        ON CONFLICT (id) DO UPDATE SET
          title = excluded.title,
          category = excluded.category,
          location = excluded.location,
          category_color = excluded.category_color,
          image = excluded.image,
          supporting_images = excluded.supporting_images,
          slug = excluded.slug,
          content = excluded.content,
          excerpt = excluded.excerpt,
          published_at = excluded.published_at,
          status = excluded.status,
          created_at = excluded.created_at,
          updated_at = excluded.updated_at
      `;
    }

    for (const comment of comments) {
      await tx`
        INSERT INTO comments (
          id, post_id, author, email, text, status, reply, created_at, updated_at
        ) VALUES (
          ${comment.id}, ${comment.post_id}, ${comment.author}, ${comment.email}, ${comment.text},
          ${comment.status || "Pending"}, ${comment.reply || ""},
          ${normalizeTimestamp(comment.created_at)}, ${normalizeTimestamp(comment.updated_at)}
        )
        ON CONFLICT (id) DO UPDATE SET
          post_id = excluded.post_id,
          author = excluded.author,
          email = excluded.email,
          text = excluded.text,
          status = excluded.status,
          reply = excluded.reply,
          created_at = excluded.created_at,
          updated_at = excluded.updated_at
      `;
    }

    for (const contact of contacts) {
      await tx`
        INSERT INTO contacts (id, name, email, message, status, created_at)
        VALUES (
          ${contact.id}, ${contact.name}, ${contact.email}, ${contact.message},
          ${contact.status || "Unread"}, ${normalizeTimestamp(contact.created_at)}
        )
        ON CONFLICT (id) DO UPDATE SET
          name = excluded.name,
          email = excluded.email,
          message = excluded.message,
          status = excluded.status,
          created_at = excluded.created_at
      `;
    }

    for (const subscriber of subscribers) {
      await tx`
        INSERT INTO newsletter_subscribers (id, email, source, status, created_at, updated_at)
        VALUES (
          ${subscriber.id}, ${subscriber.email}, ${subscriber.source || "blog"},
          ${subscriber.status || "Subscribed"}, ${normalizeTimestamp(subscriber.created_at)},
          ${normalizeTimestamp(subscriber.updated_at)}
        )
        ON CONFLICT (email) DO UPDATE SET
          source = excluded.source,
          status = excluded.status,
          created_at = excluded.created_at,
          updated_at = excluded.updated_at
      `;
    }
  });

  await resetIdentity("posts");
  await resetIdentity("comments");
  await resetIdentity("contacts");
  await resetIdentity("newsletter_subscribers");

  console.log(
    `Migrated ${posts.length} posts, ${comments.length} comments, ${contacts.length} contacts, and ${subscribers.length} subscribers to Postgres.`,
  );
}

try {
  await migrate();
} finally {
  source.close();
  await sql.end({ timeout: 5 });
}
